"""
+ - adicao
- - subtracao
* - multiplicacao
/ - divicao
// - divicao inteira
** - potenciacao
% - modulo
() - alterar precedencia
"""

print('Multiplicacao * ', 10 * 10)
print('Adicao + ', 10 + 10)
print('Subtracao - ', 10 - 5)
print('Divicao / ', 10 / 2)
print("Divisao / ", 10 / 3)  # Divisao onde é arredondado o valor divido
print("Divisao Inteiro // ", 10 // 3)  # Divisao onde é divido com casas decimais
print("Potenciaçao **", 2 ** 10)  # 2 Elevado 10° potencia
print("Resto da divisao ", 10 % 3)  # É feito a divicao e mostrado o resto do calculo
print('Precedencia de calculo () ', 5+(2*10))  # É sempre calculado primeiro o que esta dentro dos parenteses

